/**
 * Outcome classes of combinatorial game.
 * N is a next player win
 * P is a previous(second) player win
 *
 * L is a left player win
 * R is a right player win
 * L and R are only for partisan games where moves are not universal
 * @author Conor Brown
 */
public enum OutcomeClass {
    N, P, L, R
}
